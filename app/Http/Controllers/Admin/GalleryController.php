    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'photopath' => 'required|image|mimes:jpeg,png,jpg,gif,svg',
        ]);

        if ($request->has('photopath')) {
            $fname = Str::random(20);
            $fexe = $request->file('photopath')->extension();
            $fpath = "$fname.$fexe";

            $request->file('photopath')->move(public_path() . '/public/galleries', $fpath);

            $data['photopath'] = 'galleries/' . $fpath;
        }

        Gallery::create($data);

        return redirect()->route('admin.galleries.index')->with('success', 'Gallery added sucessfully');
    }

